/*Lógica
1- Criar uma array
2- Atrubuir Valores dentro dela
3- Percorrer todos os elementos da array utilizando for
4- Quando encontrar o primeiro elemento, imprimir ele no console
5- acrescentar + 1 para seguir para o próximo elemento

*/

let numbers = [5, 9, 3, 19, 70, 8, 100, 2, 35, 27];

for (let i = 0; i < numbers.length; i +=1){
    console.log(numbers[i]);
}

